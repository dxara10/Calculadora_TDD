class Exponenciacao:
    """Classe para realizar a exponenciação."""

    def executar(self, base: float, exp: float) -> float:
        """USO DO COPILOT PARA ESTA DOCSTRING: Calcula a exponenciação de uma base elevada a um expoente.

        Args:
            base (float): A base a ser elevada.
            exp (float): O expoente ao qual a base será elevada.

        Returns:
            float: O resultado da exponenciação.
        """
        return base ** exp