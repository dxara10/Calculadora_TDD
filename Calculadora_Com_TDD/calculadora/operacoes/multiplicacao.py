class Multiplicacao:
    """USO DO COPILOT PARA ESTA DOCSTRING: Classe para realizar a operação de multiplicação."""

    def executar(self, a: float, b: float) -> float:
        """Executa a multiplicação entre dois números.

        Parâmetros:
        a (float): O primeiro número.
        b (float): O segundo número.

        Retorna:
        float: O resultado da multiplicação de a por b.
        """
        return a * b