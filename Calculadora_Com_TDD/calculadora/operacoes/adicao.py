class Adicao:
    """Classe para realizar a operação de adição."""

    def executar(self, a: float, b: float) -> float:
        """USO DO COPILOT PARA ESTA DOCSTRING:Executa a adição entre dois números.

        Parâmetros:
        a (float): O primeiro número.
        b (float): O segundo número.

        Retorna:
        float: O resultado da adição de a e b.
        """
        return a + b