class Subtracao:
    """USO DO COPILOT PARA ESTA DOCSTRING: Classe para realizar a operação de subtração."""

    def executar(self, a: float, b: float) -> float:
        """Executa a subtração entre dois números.

        Args:
            a (float): O minuendo.
            b (float): O subtraendo.

        Returns:
            float: O resultado da subtração (a - b).
        """
        return a - b